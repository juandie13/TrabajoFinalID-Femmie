    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // TODO add your handling code here:
        if (jTextDNI.getText().isEmpty() || jTextNombre.getText().isEmpty() 
                || jTextCorreo.getText().isEmpty() || jTextDireccion.getText().isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "Llenar todos los campos.");
        }
        else
        {
            Cliente cli = new Cliente();
            //Telefono tel = new Telefono();
            cli.setDNI(jTextDNI.getText());
            cli.setNombre(jTextNombre.getText());
            cli.setCorreo(jTextCorreo.getText());
            cli.setDireccion(jTextDireccion.getText());
            //cli.setTelefono(Integer.parseInt(jTextTelefono.getText()));
            
            String mensaje = cliBO.registrarCliente(cli);
            
            JOptionPane.showMessageDialog(null, mensaje);
            borrar();
            listarCliente();
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed


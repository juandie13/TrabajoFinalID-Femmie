    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        if (jTextPedido.getText().isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "Llenar todos los campos.");
        }
        else
        {            
            String mensaje = pedBO.eliminarPedido(jTextPedido.getText());
            JOptionPane.showMessageDialog(null, mensaje);
            borrar();
            listarPedido();
        }
        
        
        
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jTablePedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePedidoMouseClicked
        // TODO add your handling code here:
         int seleccion = jTablePedido.rowAtPoint(evt.getPoint());
        jTextPedido.setText(jTablePedido.getValueAt(seleccion, 0)+"");
        jComboBoxPromocion.setSelectedItem(jTablePedido.getValueAt(seleccion, 1));
        jComboBoxDelivery.setSelectedItem(jTablePedido.getValueAt(seleccion, 2));
        jTextFechaR.setText(jTablePedido.getValueAt(seleccion, 3)+"");
        jTextFechaE.setText(jTablePedido.getValueAt(seleccion, 4)+"");
        
        
        
    }//GEN-LAST:event_jTablePedidoMouseClicked


    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // TODO add your handling code here:
        
        if ( jTextPedido.getText().isEmpty() 
                || jTextFechaR.getText().isEmpty() ) 
        {
            JOptionPane.showMessageDialog(null, "Llenar todos los campos.");
        }
        else
        {
            Pedido ped = new Pedido();
           
            ped.setNumPedido(jTextPedido.getText());
            ped.setFechaRegistro(jTextFechaR.getText());
            ped.setFechaEntrega(jTextFechaE.getText());
 
            // DELIVERY
            if(jComboBoxDelivery.getSelectedIndex() == 1)
            {
                ped.setCodDelivery((String) jComboBoxDelivery.getSelectedItem());
            }
            
            else if(jComboBoxDelivery.getSelectedIndex() == 2)
            {
                ped.setCodDelivery((String) jComboBoxDelivery.getSelectedItem());
            }
            
            else if(jComboBoxDelivery.getSelectedIndex() == 3)
            {
                ped.setCodDelivery((String) jComboBoxDelivery.getSelectedItem());
            }
            
            else
            {
                ped.setCodDelivery(null);
            }
            
            // PROMOCION
            if(jComboBoxPromocion.getSelectedIndex() == 1)
            {
                ped.setNumPromocion((String) jComboBoxPromocion.getSelectedItem());
            }
            
            else if(jComboBoxPromocion.getSelectedIndex() == 2)
            {
                ped.setNumPromocion((String) jComboBoxPromocion.getSelectedItem());
            }
            
            else if(jComboBoxPromocion.getSelectedIndex() == 3)
            {
                ped.setNumPromocion((String) jComboBoxPromocion.getSelectedItem());
            }
            
            else if(jComboBoxPromocion.getSelectedIndex() == 4)
            {
                ped.setNumPromocion((String) jComboBoxPromocion.getSelectedItem());
            }
            
            else
            {
                ped.setNumPromocion(null);
            }
            
            String mensaje = pedBO.registrarPedido(ped);
            
            JOptionPane.showMessageDialog(null, mensaje);
            borrar();
            listarPedido();
        }
        
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

